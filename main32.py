# Решение
import pandas as pd
import matplotlib.pyplot as plt

# Загрузка данных
df = pd.read_csv('dataset/IQ_countries.csv')

# Создаем scatter plot для связи между средним IQ и количеством Нобелевских премий
plt.figure(figsize=(10, 6))
plt.scatter(df['Average IQ'], df['Nobel Prices'], alpha=0.7)
plt.title('Связь между средним IQ и количеством Нобелевских премий')
plt.xlabel('Средний IQ')
plt.ylabel('Количество Нобелевских премий')
plt.grid(True)
plt.show()

# Создаем scatter plot для связи между средним IQ и индексом человеческого развития
plt.figure(figsize=(10, 6))
plt.scatter(df['Average IQ'], df['Human Development Index'], alpha=0.7)
plt.title('Связь между средним IQ и индексом человеческого развития')
plt.xlabel('Средний IQ')
plt.ylabel('Индекс человеческого развития')
plt.grid(True)
plt.show()

# Создаем scatter plot для связи между средним IQ и численностью населения
plt.figure(figsize=(10, 6))
plt.scatter(df['Average IQ'], df['Population'].astype(str), alpha=0.7)
plt.title('Связь между средним IQ и численностью населения')
plt.xlabel('Средний IQ')
plt.ylabel('Численность населения')
plt.grid(True)
plt.show()

# Создаем scatter plot для связи между средним IQ и уровнем грамотности
plt.figure(figsize=(10, 6))
plt.scatter(df['Average IQ'], df['Literacy Rate'], alpha=0.7)
plt.title('Связь между средним IQ и уровнем грамотности')
plt.xlabel('Средний IQ')
plt.ylabel('Уровень грамотности')
plt.grid(True)
plt.show()

# Создаем scatter plot для связи между средним IQ и валовым национальным доходом
plt.figure(figsize=(10, 6))
plt.scatter(df['Average IQ'], df['Gross National Income'], alpha=0.7)
plt.title('Связь между средним IQ и валовым национальным доходом')
plt.xlabel('Средний IQ')
plt.ylabel('Валовой национальный доход')
plt.grid(True)
plt.show()
