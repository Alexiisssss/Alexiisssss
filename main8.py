# Задача 8. Какого цвета клетка?
# Условие. Клетки на шахматной доске идентифицируются буквой и цифрой. Буква определяет положение клетки по горизонтали, а цифра – по вертикали, как показано на рисунке.
#

#
# Ваша программа должна запрашивать у пользователя координаты клетки.
# Используйте условное выражение для определения того, с какой клетки – белой или черной – начинается столбец.
# Затем при помощи обычной арифметики необходимо определить цвет конкретной клетки.
# Например, если пользователь ввел a1, программа должна определить, что клетка с этими координатами черная.
# Если d5 – белая.



# Решение
def check_chessboard_color(cell):
    if len(cell) != 2 or not cell[0].isalpha() or not cell[1].isdigit():
        print("Ошибка: Введите координаты в формате буква и цифра (например, a1).")
        return

    column = cell[0].lower()
    row = int(cell[1])

    if (column in "aceg" and row % 2 == 1) or (column in "bdfh" and row % 2 == 0):
        cell_color = "черный"
    else:
        cell_color = "белый"

    print(f"Клетка {cell} имеет {cell_color} цвет.")

try:
    user_input = input("Введите координаты клетки (например, a1): ")
    check_chessboard_color(user_input)
except Exception as e:
    print(f"Ошибка: {e}")
